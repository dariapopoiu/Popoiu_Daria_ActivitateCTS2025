package clase;

public class Persoana {
    private int varsta;
    private String nume;

    public Persoana(int varsta, String nume) {
        this.varsta = varsta;
        this.nume = nume;
    }

    public int getVarsta() {
        return varsta;
    }

    public String getNume() {
        return nume;
    }
}
